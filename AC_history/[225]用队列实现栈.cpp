class MyStack
{
private:
    queue<int> q;

public:
    /** Initialize your data structure here. */
    MyStack()
    {

    }

    /** Push element x onto stack. */
    // 1 2 3 4 -> 4 1 2 3
    void push(int x)
    {
        q.push(x);
    }

    /** Removes the element on top of the stack and returns that element. */
    int pop()
    {
        int tmp;
        for (int i = 0; i < q.size() - 1; ++i)
        {
            tmp = q.front();
            q.pop();
            q.push(tmp);
        }
        tmp = q.front();
        q.pop();
        return tmp;
    }

    /** Get the top element. */
    int top()
    {
        int ret = this->pop();
        this->push(ret);
        return ret;
    }

    /** Returns whether the stack is empty. */
    bool empty()
    {
        return q.empty();
    }


};

