/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:


    // ------------------------- back track --------------------------------

    vector<vector<int> > ans;

    void backtrack(TreeNode* root, int depth, vector<int> path)
    {
        if (!root) return;
        if (!root->left && !root->right)
        {
            path.push_back(root->val);
            ans.push_back(path);
            return;
        }
        path.push_back(root->val);
        backtrack(root->left, depth + 1, path);
        backtrack(root->right, depth + 1, path);
        path.pop_back();
    }

    int sumNumbers1(TreeNode* root)
    {
        if (!root) return 0;
        if (!root->left && !root->right) return root->val;
        vector<int> path;
        backtrack(root, 1, path);

        int sum = 0;
        for (int i = 0; i < ans.size(); ++i)
        {
            // vector<int> -> int
            int flag = 0;
            for (int j = 0; j < ans[i].size(); ++j)
            {
                flag = ans[i][j] + flag * 10;
            }
            sum += flag;
        }
        return sum;
    }

    // ---------------------------- dfs --------------------------


    int sum = 0;

    void dfs(TreeNode* root, int v)
    {
        if (!root) return;
        v = v * 10 + root->val;
        if (!root->left && !root->right) sum += v;
        dfs(root->left, v);
        dfs(root->right, v);
    }

    int sumNumbers(TreeNode* root)
    {
        int v = 0;
        dfs(root, v);
        return sum;
    }

};
