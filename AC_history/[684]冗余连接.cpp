class Solution
{
public:

    // æ ‘æŒ‡çš„æ˜¯ä¸€ä¸ªè¿é€šä¸”æ— ç¯çš„æ— å‘å›¾ï¼Œå¦‚æœæœ‰å¤šä¸ªç­”æ¡ˆï¼Œåˆ™è¿”å›äºŒç»´æ•°ç»„ä¸­æœ€åå‡ºç°çš„è¾¹
    // æœ€åæ„æˆç¯çš„ä¹Ÿä¸€å®šæ˜¯æœ€åå‡ºç°çš„è¾¹
    // æ€è·¯ï¼š
    // è¿”å›æœ€å…ˆæ„æˆç¯çš„ edge
    // 1. æ„é€ å¹¶æŸ¥é›†
    // 2. åˆ¤æ–­edgeçš„ä¸¤ä¸ªèŠ‚ç‚¹æ˜¯å¦éƒ½å±äºä¸€ä¸ªé›†åˆï¼Œå¦‚æœéƒ½å±äºä¸€ä¸ªé›†åˆï¼Œé‚£ä¹ˆè¿”å›è¿™ä¸ªedge
    // è¿™å¯çœŸæ˜¯ä¸€é“è‰¯å¿ƒé¢˜ç›®å•Šï¼ŒçœŸç®€å•
    vector<int> findRedundantConnection(vector <vector<int>> &edges)
    {
        UnionFind uf(edges.size());
        for (vector<int> edge:edges)
        {
            int x = edge[0] - 1;
            int y = edge[1] - 1;
            if (uf.find(x) == uf.find(y)) return edge;
            uf.unite(x, y);
        }
        return {};
    }


private:

    class UnionFind
    {
    private:
        vector<int> parent;
        vector<int> rank;

    public:

        UnionFind(int N)
        {
            parent = vector<int>(N);
            rank = vector<int>(N);
            for (int i = 0; i < N; ++i)
            {
                parent[i] = i;
                rank[i] = 1;
            }
        }

        int find(int x)
        {
            if (x != parent[x])
            {
                parent[x] = find(parent[x]);
            }
            return parent[x];
        }

        void unite(int x, int y)
        {
            int px = find(x);
            int py = find(y);

            if (px != py)
            {
                if (rank[px] > rank[py])
                {
                    parent[py] = px;
                } else if (rank[px] < rank[py])
                {
                    parent[px] = py;
                } else {
                    parent[px] = py;
                    rank[py]++;
                }
            }
        }

    };
};
