class Solution {
public:

    vector<string> ans;

    void backtrack(string &digits, int depth, string &path)
    {
        if (depth == digits.size())
        {
            ans.push_back(path);
            return;
        }
        string choices = letterMap(digits[depth]);

        for (int i = 0; i < choices.size(); ++i)
        {
            path.push_back(choices[i]);
            backtrack(digits, depth + 1, path);
            path.pop_back();
        }
    }

    vector<string> letterCombinations(string digits)
    {
        if (digits.empty()) return {};
        string path = "";
        backtrack(digits, 0, path);
        return ans;
    }


private:

    /**
     *   put('2', "abc");
            put('3', "def");
            put('4', "ghi");
            put('5', "jkl");
            put('6', "mno");
            put('7', "pqrs");
            put('8', "tuv");
            put('9', "wxyz");
     */
    string letterMap(char a)
    {
        switch (a)
        {
            case '2': return "abc";
            case '3': return "def";
            case '4': return "ghi";
            case '5': return "jkl";
            case '6': return "mno";
            case '7': return "pqrs";
            case '8': return "tuv";
            case '9': return "wxyz";
            default:
                return "";
        }
        return "";
    }
};
