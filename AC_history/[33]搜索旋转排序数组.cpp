    int search(vector<int> &nums, int target)
    {
        int l = 0, r = nums.size() - 1, mid;
        while(l <= r)
        {
            mid = l + (r - l >> 1);
            if (nums[mid] == target) return mid;
            else if (nums[l] <= nums[mid])
            {
                if (nums[l] <= target && nums[mid] > target)
                {
                    r = mid - 1;
                } else {
                    l = mid + 1;
                }
            } else if (nums[l] > nums[mid])
            {
                if (nums[mid] < target && nums[r] >= target)
                {
                    l = mid + 1;
                } else {
                    r = mid - 1;
                }
            }
        }
        return -1;
    }

