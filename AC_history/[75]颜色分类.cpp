    // ç¬¬äºŒæ¬¡ï¼ŒæŠŠæ‰€æœ‰çš„ 2 æ”¾åœ¨åé¢ï¼Œç±»ä¼¼é›¶ç§»åŠ¨
    void sortColors1(vector<int> &nums)
    {
        int ptr = 0;
        for (int i = 0; i < nums.size(); ++i)
        {
            if (nums[i] == 0)
            {
                swap(nums[ptr++], nums[i]);
            }
        }

        ptr = nums.size() - 1;
        for (int i = nums.size() - 1; i >= 0; --i)
        {
            if (nums[i] == 2)
            {
                swap(nums[ptr--], nums[i]);
            }
        }
    }

    // æ–¹æ³•ä¸‰ åŸºäºåŒæŒ‡é’ˆçš„ä¸€æ¬¡éå†
