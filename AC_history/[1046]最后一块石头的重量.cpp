    int lastStoneWeight1(vector<int> &stones)
    {
        if (stones.empty()) return 0;
        if (stones.size() == 1) return stones.back();
        priority_queue<int, vector<int>, less<int> > q;  // å¤§é¡¶å †
        for (int stone:stones) q.push(stone);
        while (q.size() > 1)
        {
            int y = q.top();
            q.pop();
            int x = q.top();
            q.pop();
            if (x == y) continue;
            else
            {
                q.push(y - x);
            }
        }
        return q.empty() ? 0 : q.top();
    }
