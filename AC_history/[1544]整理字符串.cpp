    string makeGood1(string s)
    {
        stack<char> sta;
        sta.push('^');
        for (int i = 0; i < s.size(); ++i)
        {
            if (sta.top() - 32 == s[i] || sta.top() + 32 == s[i])
            {
                sta.pop();
            } else
            {
                sta.push(s[i]);
            }
        }
        string ret = "";
        while (sta.size() > 1)
        {
            ret = sta.top() + ret;
            sta.pop();
        }
        return ret;
    }

