    int findMin(vector<int> &nums)
    {
        int l = 0, r = nums.size() - 1, mid;
        while (l <= r)
        {
            mid = l + (r - l >> 1);
            if (nums[l] <= nums[r]) return nums[l];
            else if (nums[l] <= nums[mid])
            {
                l = mid + 1;
            } else
            {
                r = mid;
            }
        }
        return -1;
    }
