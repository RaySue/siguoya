    // 2. ä»å°åˆ°å¤§å¯¹æ¯ä¸ªæ¡¶è¿›è¡Œç´¯åŠ ï¼Œå³å¾—åˆ°å°äºè¯¥ä»·æ ¼çš„ä¸»é£Ÿçš„ç§ç±»
    // 3. éå† drinksï¼Œé€šè¿‡ x - drinks[i] çš„

