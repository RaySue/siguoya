    // å¯¹stapleæ’åºï¼Œç„¶åéå†drinksï¼Œå¦‚æœ drinks[i] > x continue
    // if drinks[i] < x é‚£ä¹ˆå°±ç´¯åŠ æ‰€æœ‰ staple ä¸­ä»·æ ¼å°äºç­‰äº x - drinks[i] çš„ä¸ªæ•°
    //
    // åˆ©ç”¨é¢å¤–çš„ç©ºé—´
