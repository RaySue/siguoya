/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution
{
public:

    bool preOrderTraverse(TreeNode *root, int benchMark)
    {
        if (!root) return true;
        if (root->val != benchMark) return false;
        return preOrderTraverse(root->left, benchMark) && preOrderTraverse(root->right, benchMark);
    }

    bool isUnivalTree(TreeNode *root)
    {
        int benchMark = root->val;
        return preOrderTraverse(root, benchMark);
    }
};
