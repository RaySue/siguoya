    bool validateStackSequences(vector<int>& pushed, vector<int>& popped)
    {
        stack<int> dummySta;
        int p = 0;
        for (int i = 0; i < pushed.size(); ++i)
        {
            dummySta.push(pushed[i]);
            while (!dummySta.empty() && p < popped.size() && dummySta.top() == popped[p])
            {
                dummySta.pop();
                p++;
            }
        }
        return dummySta.empty();
    }
